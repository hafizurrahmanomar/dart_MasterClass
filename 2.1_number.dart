void main() {
  // declare an integer
  int num1 = 10;

  // declare a double value
  double num2 = 10.50;

  // print the values
  print(num1);
  print(num2);

  // integer
  final int myAge = 35;
  print(myAge);

  //double
  const double num = 4.85;
  print(num);

  // final keyword =>A variable with the final keyword will be initialized at runtime and can only be assigned for a single time
  final Age = 25;
  print(Age);

  // const keyword =>A variable with the const keyword is initialized at compile-time and is already assigned when at runtime.
  const Num = 5.803;
  print(Num);
  
}
