void main() {
  // type safety
  print("Dart Good Practice");
  int a = 10;
  int b = 20;

  double num1 = 10.4;
  double num2 = 6.5;

  int sum = a + b;
  double sum1 = num1 + num2;

  print(sum);
  print(sum1);
  // string
  String name1 = "Hafizur";
  String name2 = "Rahman";
  print(name1 + " " + name2);

  // boolean for true / false
  bool myBool = false;
  print(myBool);
}

  